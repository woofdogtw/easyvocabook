/// Simple static locale lookup. `lang` is "en", "zh-TW", or "zh-CN".
/// Returns the translation for `key`, falling back to English, then to `key` itself.
pub fn t(lang: &str, key: &'static str) -> &'static str {
    let zh_tw = match lang {
        "zh-TW" | "zh-CN" => true,
        _ => false,
    };
    let simplified = lang == "zh-CN";

    // Fast dispatch: if English just look up in the en table, otherwise
    // try zh-TW first (zh-CN overrides come after).
    if !zh_tw {
        return en(key);
    }
    if simplified {
        zh_cn(key).unwrap_or_else(|| zh_tw_str(key).unwrap_or_else(|| en(key)))
    } else {
        zh_tw_str(key).unwrap_or_else(|| en(key))
    }
}

